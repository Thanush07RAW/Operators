public class Operatorstypes {
    public static void main(String[] args) {
        int p = 18, q = 12;

        // Arithmetic
        System.out.println("Addition: " + (p + q));
        System.out.println("Subtraction: " + (p - q));

        // Relational
        System.out.println("a < b: " + (p < q));

        // Logical
        System.out.println("a < b && b > 15: " + (p < q && q > 15));
    }
}

